package secao3

fun main(){

    //AND
    println(true && true)
    println(true && false)
    println(false && true)
    println(false && false)

    println()

    //OR
    println(true || true)
    println(true || false)
    println(false || true)
    println(false || false)

    println()

    //NOT
    println(!true)
    println(!false)
}